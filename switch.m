%定义一个字符数组 daystring

switch daystring
    
    case 'Monday'
        disp('Start of the work week')
    case  'Tuesday'
        disp('day 2')
    case 'Wednesday'
        disp('day 3')
    case 'Thursday'
        disp('day 4')
    case 'Friday'
        disp('day 5')
    case 'Saturday'
        disp('End of the work week')
    case 'Sunday'
        disp('Weekend')